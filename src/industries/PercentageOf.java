package industries;

public class PercentageOf {
	private int percentageOf;
	
	public PercentageOf() {
		this(0);
	}
	
	public PercentageOf(int percentage) {
		percentageOf = percentage;
	}

	public int getPercentageOf() {
		return percentageOf;
	}

	public void setPercentageOf(int percentageOf) {
		this.percentageOf = percentageOf;
	}
	
	

}
